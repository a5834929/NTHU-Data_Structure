node_pointer AdjList[N]={0};

void ConstructList()
{
    int row, col;
    node_pointer temp, target;
    for(row=0;row<N;row++)
    {
        for(col=0;col<N;col++)
        {
            if(AdjMatrix[row][col])
            {
                temp=(node_pointer)malloc(sizeof(node));
                temp->data=col;
                temp->next=NULL;

                if(AdjList[row]==NULL)
                    AdjList[row]=temp;

                else
                {
                    target=AdjList[row];
                    while(target->next!=NULL)
                    {
                        target=target->next;
                    }
                    target->next=temp;
                }
            }
        }
    }
    return;
}

